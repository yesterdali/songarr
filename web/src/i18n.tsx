export type MessageKey = keyof typeof messages.en;

type I18nValue = {
  language: Language;
  locale: string;
  setLanguage: (language: Language) => void;
  t: (key: MessageKey, vars?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

export function detectLanguage(input?: string | readonly string[]): Language {
  const candidates = Array.isArray(input)
    ? input
    : input
      ? [input]
      : typeof navigator !== "undefined"
        ? [navigator.language, ...(navigator.languages ?? [])]
        : [];
  for (const candidate of candidates) {
    const prefix = candidate.toLowerCase().split("-")[0];
    if (prefix === "en" || prefix === "de" || prefix === "ru") return prefix;
  }
  return "en";
}

export function loadLanguage(storage: Storage | null = safeStorage()): Language {
  const stored = storage?.getItem(LANGUAGE_KEY);
  if (stored === "en" || stored === "de" || stored === "ru") return stored;
  return detectLanguage();
}

export function saveLanguage(language: Language, storage: Storage | null = safeStorage()): void {
  storage?.setItem(LANGUAGE_KEY, language);
}

function safeStorage(): Storage | null {
  try {
    return typeof window === "undefined" ? null : window.localStorage;
  } catch {
    return null;
  }
}

export function translate(
  language: Language,
  key: MessageKey,
  vars: Record<string, string | number> = {},
): string {
  const template = messages[language][key] ?? messages.en[key] ?? key;
  return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? ""));
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => loadLanguage());
  const setLanguage = useCallback((next: Language) => {
    saveLanguage(next);
    setLanguageState(next);
  }, []);
  const t = useCallback<I18nValue["t"]>(
    (key, vars) => translate(language, key, vars),
    [language],
  );
  const value = useMemo<I18nValue>(
    () => ({ language, locale: language, setLanguage, t }),
    [language, setLanguage, t],
  );
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n(): I18nValue {
  const value = useContext(I18nContext);
  if (!value) throw new Error("useI18n used outside I18nProvider");
  return value;
}

export function messageKeys(): MessageKey[] {
  return Object.keys(messages.en) as MessageKey[];
}

export function hasMessage(language: Language, key: MessageKey): boolean {
  return Boolean(messages[language][key]);
}
